/**
 * Returns true if the password (case-insensitive) matches a known weak
 * choice. We compare lowercased because "Password" and "PASSWORD" are
 * just as dangerous as "password".
 */
export function isCommonPassword(plain: string): boolean {
  return COMMON_PASSWORDS.has(plain.toLowerCase());
}

/**
 * Returns true if the password is trivially derivable from the email —
 * specifically when it equals or is contained in the local-part. Catches
 * "alice@example.com" with password "alice" or "alice1".
 */
export function passwordContainsEmail(
  plain: string,
  email: string,
): boolean {
  const local = email.split("@")[0]?.toLowerCase() ?? "";
  if (local.length < 3) return false;
  const lower = plain.toLowerCase();
  return lower === local || lower.includes(local);
}
